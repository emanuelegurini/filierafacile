package com.filiera.facile.model.enums;

/**
 * Definisce i principali metodi di coltivazione per le materie prime.
 */
public enum MetodoColtivazione {
    /**
     * Senza pesticidi chimici, solo sostanze naturali.
     */

    BIOLOGICO,

    /**
     * Approccio olistico che segue i cicli lunari e preparati naturali.
     */
    BIODINAMICO,

    /**
     * Uso ridotto e mirato di chimici, combinato con pratiche ecologiche.
     */
    AGRICOLTURA_INTEGRATA,

    /**
     * Coltivazione intensiva con largo uso di chimici e fertilizzanti.
     */
    CONVENZIONALE,

    /**
     * Coltura fuori suolo con nutrienti disciolti in acqua..
     */
    IDROPONICO,

    /**
     * Sistema sostenibile che imita gli ecosistemi naturali.
     */
    PERMACULTURA
}
